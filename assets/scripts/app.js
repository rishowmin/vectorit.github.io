$(document).ready(function () {
    var flag = 0;
    $("#main-service-inner-1").hover(function () {
        if (flag == 0) {
            $('#service-descrip-1').css('display', 'block');
            $('#service-descrip-1').css('transform', 'rotateY(180deg)');
            $('#service-cover-1').css('display', 'none');
            flag = 1;
        } else if (flag == 1) {
            $('#service-descrip-1').css('display', 'none');
            $('#service-cover-1').css('display', 'block');
            flag = 0;
        }
    });
});

